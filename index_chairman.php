      <h1>About PBI</h1>
      <hr></hr>
      <p>The academic curriculum of PDPM Indian Institute of Information Technology, Design & Manufacturing (IIITDM) Jabalpur focuses very strongly on hands on experience, interdisciplinary education and project oriented learning. Its agenda is to produce graduates who are not only technically competent but also possess other skills like capability to learn through experience, critical thinking, practical aptitude and ability to synthesize the solution. It also recognizes that not all aspects of learning can be taught in the conventional way of classroom (or laboratory) teaching methodology. Realizing that there are important elements of learning in an organization, the Institute has opened its academic programme for approximately six months long project-based internship (PBI) opportunity to its students to be executed after the completion of sixth semester. The internship aims to provide on-the-job experience or exposure to ongoing research and development in an organization under the supervision of able practitioners/researchers. The internship would contribute to the development of a student's comprehension on technical skills, knowledge and practical problems.</p>
     
